// Exemplo com nomes descritivos:
let precoProduto = 10;
let quantidadeComprada = 20;
let valorTotal = precoProduto * quantidadeComprada;

console.log("Resultado bom:", valorTotal);

// Muito mais claro:
// - precoProduto → valor do item
// - quantidadeComprada → quantos itens
// - valorTotal → resultado final

// Nome descritivo = código mais legível, menos erros.
