// Isso significa que o tipo da variável é decidido automaticamente
// pelo valor que você coloca nela.

// Exemplo 1: variável começando como número
let idade = 15;
console.log("Idade:", idade);

// Agora a mesma variável vira uma string (texto)
idade = "quinze";
console.log("Agora idade virou:", idade);

