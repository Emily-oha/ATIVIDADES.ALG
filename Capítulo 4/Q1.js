// Uma variável é um espaço na memória onde guardamos um valor.
// Ela pode mudar durante o programa.

// Criando variáveis:
let nome = "Emily";
let idade = 15;

// Exibindo valores:
console.log("Nome:", nome);
console.log("Idade:", idade);

// Variáveis são necessárias para guardar valores e fazer cálculos:
let nota1 = 8;
let nota2 = 9;

// Cálculo usando variáveis:
let media = (nota1 + nota2) / 2;

console.log("Média:", media);
